public class Operators {
    public static void main(String[] args)
    {
        int number1 = 13;
        int number2 = 4;
        System.out.println(number1 + number2);
        System.out.println(number1 - number2);
        System.out.println(number1 * number2);
        System.out.println(number1 / number2);
        System.out.println(number1 % number2);
    }
}
